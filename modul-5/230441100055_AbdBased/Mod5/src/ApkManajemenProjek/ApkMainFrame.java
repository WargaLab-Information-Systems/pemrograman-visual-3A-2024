    private void BtnSimpanTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanTransaksiActionPerformed
        // TODO add your handling code here:
        try {
            String idTransaksi = txtIDTransaksi.getText().trim();
 
            String pilihanKaryawan = (String) cbKaryawan.getSelectedItem();
            String pilihanProyek = (String) cbProyek.getSelectedItem();
            if (pilihanKaryawan == null || pilihanKaryawan.equals("--- PILIH KARYAWAN ---")) {
                JOptionPane.showMessageDialog(this, "Silakan pilih karyawan.","Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (pilihanProyek == null || pilihanProyek.equals("--- PILIH PROYEK ---")) {
                JOptionPane.showMessageDialog(this, "Silakan pilih proyek.","Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
            String[] parts = pilihanKaryawan.split(",");
            String idKaryawan = parts[0]; 

            String[] parts2 = pilihanProyek.split(",");
            String idProyek = parts2[0];
            String sql = "INSERT INTO tb_transaksi (id_transaksi, id_karyawan, id_proyek, peran) VALUES (?, ?, ?, ?)";
            java.sql.Connection conn = (Connection) Koneksi.getKoneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            String peranKaryawan = txtPeran.getText().trim();
            if (peranKaryawan.isEmpty() || !peranKaryawan.matches("^[a-zA-Z\\s]+$")) {
                JOptionPane.showMessageDialog(null, "Peran harus diisi dengan huruf dan spasi saja!","Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }


            pst.setString(1, idTransaksi);
            pst.setString(2, idKaryawan);
            pst.setString(3, idProyek);
            pst.setString(4, peranKaryawan);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data transaksi berhasil disimpan.","Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
            load_table3();
            ResetTransaksi();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_BtnSimpanTransaksiActionPerformed

