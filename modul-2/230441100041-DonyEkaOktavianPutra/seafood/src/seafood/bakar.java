    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
        int hasilkabeh = Integer.parseInt(Jpesanan.getText());
   
        int hargasemua = 0;
        int diskon = 0;
        int hargasaus = 0;
     
        
        
    
        if(scalop.isSelected()){
            hargasemua=15000 * hasilkabeh;
        }else if(stikcrab.isSelected()){
            hargasemua=25000 * hasilkabeh;
        }else if(lobster.isSelected()){
            hargasemua=20000 * hasilkabeh;
        }else if(oster.isSelected()){
            hargasemua=30000 * hasilkabeh;
        }else if(sop.isSelected()){
            hargasemua=18000 * hasilkabeh;
        }else if(squid.isSelected()){
            hargasemua=39000 * hasilkabeh;
        }else{
            JOptionPane.showMessageDialog(this,"Mohon Pilih Pesanan","COBA LAGI",JOptionPane.WARNING_MESSAGE,silang);
            return;
    }
    
        if(padang.isSelected()){
            hargasaus += 5000 * hasilkabeh;
        }else if (bolones.isSelected()){
            hargasaus += 6000 * hasilkabeh;
        }else if (tiram.isSelected()){
            hargasaus += 7000 * hasilkabeh;
        }else {
            JOptionPane.showMessageDialog(this,"Mohon Pilih saus","COBA LAGI",JOptionPane.WARNING_MESSAGE,silang);
        }
        
        
        int jukuk = Integer.parseInt(Jpesanan.getText());
        if (jukuk == 0){
            JOptionPane.showMessageDialog(this,"Mohon Tambahkan Pesanan","COBA LAGI",JOptionPane.WARNING_MESSAGE,silang);
        }
        
        
    hargasemua += hargasaus;
    isiharganormal.setText(String.valueOf(hargasemua));
     
            if (hargasemua > 100000) {
                diskon = hargasemua * 5/100;
                hargasemua = hargasemua-diskon;
                JOptionPane.showMessageDialog(this,"Selamat Anda Mendapat Diskon 5%","yeey",JOptionPane.WARNING_MESSAGE,sukses);
        }
    
    
    hasiltotal.setText(String.valueOf(hargasemua));
    isidiskon.setText(String.valueOf(diskon));
    
        
    }//GEN-LAST:event_totalActionPerformed

