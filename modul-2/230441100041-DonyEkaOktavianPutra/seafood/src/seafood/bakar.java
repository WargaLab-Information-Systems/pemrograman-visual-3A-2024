    private void isicashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_isicashKeyReleased
        // action event pada Textfile Casg pakai Keyrelease 
         try{
            int harga_total = Integer.parseInt(hasiltotal.getText());
            int susuk = Integer.parseInt(isicash.getText());
            if (susuk < harga_total){
            isikembali.setText("Uang anda kurang");
        }   else {
            int kembalian = susuk - harga_total;
            isikembali.setText(String.valueOf(kembalian));        
        }   
        }   catch (NumberFormatException e) {
            isikembali.setText("Masukkan angka saja");
        }
    }//GEN-LAST:event_isicashKeyReleased

