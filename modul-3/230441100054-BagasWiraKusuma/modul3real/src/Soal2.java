    private void buttonBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBayarActionPerformed
        DefaultTableModel iniTable = (DefaultTableModel) tabelSpp.getModel();
        JCheckBox [] chekbok = {CBjanuari, CBfebruari,CBmaret,CBapril,CBmei,CBjuni,CBjuli,CBagustus,CBseptember,CBoktober,CBnovember,CBdesember };
        Object[] rowData = new Object[15];

        String nama = Nama.getText();
        rowData[0] = nama;

        int perulangan = 0;
        int total = 0;
        for (JCheckBox inibox : chekbok){
            perulangan+=1;
            if(inibox.isSelected() == true){
                rowData[perulangan] = "lunas";
                total+=1;
            }else{
                rowData[perulangan] = "tunggak";
            }
        }

        iniTable.addRow(rowData);
        int totalAkhir = total*500000;
        Icon ceklis = null;
        JOptionPane.showMessageDialog(this, "Pembayaran Berhasil Ditambahkan","INFO", JOptionPane.WARNING_MESSAGE,ceklis);
        totalTagihan.setText(String.valueOf(totalAkhir));
    }//GEN-LAST:event_buttonBayarActionPerformed

    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabelSpp.getModel();
        model.setRowCount(0);
        JCheckBox[] checkBoxes = {CBjanuari, CBfebruari,CBmaret,CBapril,CBmei,CBjuni,CBjuli,CBagustus,CBseptember,CBoktober,CBnovember,CBdesember }; // List of checkboxes

        for (JCheckBox checkBox : checkBoxes) {
            checkBox.setSelected(false); // Uncheck each checkbox
        }
        Nama.setText("");
        totalTagihan.setText("");
        Icon ceklis = null;
        JOptionPane.showMessageDialog(this, "Berhasil di Reset","INFO", JOptionPane.WARNING_MESSAGE,ceklis);
    }//GEN-LAST:event_resetActionPerformed

