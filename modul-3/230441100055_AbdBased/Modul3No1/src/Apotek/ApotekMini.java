    private void BtnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPilihActionPerformed
        // TODO add your handling code here:
        if (!Obat1.isSelected()&&!Obat2.isSelected()&&!Obat3.isSelected()&&!Obat4.isSelected()&&!Obat5.isSelected()
                &&!Obat6.isSelected()&&!Obat7.isSelected()&&!Obat8.isSelected()&&!Obat9.isSelected()&&!Obat10.isSelected()){
            JOptionPane.showMessageDialog(null, "Pilih minimal 1 obat dulu.", "Anda Belum Memilih Obat", JOptionPane.WARNING_MESSAGE, iconWarning);
        }else {
        // Cek apakah Obat1 atau Obat2 dipilih
        if (Obat1.isSelected() || Obat2.isSelected() || Obat3.isSelected() || Obat4.isSelected() || Obat5.isSelected()
                || Obat6.isSelected() || Obat7.isSelected() || Obat8.isSelected() || Obat9.isSelected() || Obat10.isSelected()) {
            String message = "Obat yang Anda dipilih : ";
            if (Obat1.isSelected()) {
                message += "Paracetamol ";
            }
            if (Obat2.isSelected()) {
                message += "Ibuproven ";
            }
            if (Obat3.isSelected()) {
                message += "Amoxicilin ";
            }
            if (Obat4.isSelected()) {
                message += "Cetirizine ";
            }
            if (Obat5.isSelected()) {
                message += "Losartan ";
            }
            if (Obat6.isSelected()) {
                message += "Diazepam ";
            }
            if (Obat7.isSelected()) {
                message += "Clopidogrel ";
            }
            if (Obat8.isSelected()) {
                message += "Runitidine ";
            }
            if (Obat9.isSelected()) {
                message += "Vitamin C (Asam Askorbat) ";
            }
            if (Obat10.isSelected()) {
                message += "Vitamin B12 ";
            }
            JOptionPane.showMessageDialog(null, message, "Obat Dipilih", JOptionPane.INFORMATION_MESSAGE, iconVerified);
        }
    }
    }//GEN-LAST:event_BtnPilihActionPerformed

