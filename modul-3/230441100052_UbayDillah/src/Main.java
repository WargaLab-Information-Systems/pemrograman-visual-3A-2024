    private void success(String obat) {
        JOptionPane.showMessageDialog(null, "Pembelian " + obat + " berhasil, silahkan ambil di meja!", "Pemberitahuan!", JOptionPane.INFORMATION_MESSAGE, iconCentang);

        deseaseBox.setSelectedIndex(0);
        obatGroup.clearSelection();
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int deseaseIndex = deseaseBox.getSelectedIndex();
        
        if (deseaseIndex == 0 && vitaminC.isSelected()) {
            success("vitamin");
            return;
        }
        
        if (deseaseIndex == 1 && insto.isSelected()) {
            success("Insto");
            return;
        }
        
        if (deseaseIndex == 2 && promag.isSelected()) {
            success("Promag");
            return;
        }
        
        if (deseaseIndex == 3 && panadol.isSelected()) {
            success("Panadol");
            return;
        }
        
        if (deseaseIndex == 4 && mefinal.isSelected()) {
            success("Mefinal");
            return;
        }
        
        if (deseaseIndex == 5 && OBHCombi.isSelected()) {
            success("OBH Combi");
            return;
        }
        
        if (deseaseIndex == 6 && mixagrip.isSelected()) {
            success("Mixagrip");
            return;
        }
        
        if (deseaseIndex == 7 && betadine.isSelected()) {
            success("Betadine");
            return;
        }
        
        if (deseaseIndex == 8 && albothyl.isSelected()) {
            success("Albothyl");
            return;
        }
        
        if (deseaseIndex == 9 && diapet.isSelected()) {
            success("Diapet");
            return;
        }
        
        if (obatGroup.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Pilih Obat Terlebih Dahulu", "Pemberitahuan!", JOptionPane.INFORMATION_MESSAGE, iconX);
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Pembelian gagal dikarenakan obat tidak cocok dengan penyakit", "Pemberitahuan!", JOptionPane.INFORMATION_MESSAGE, iconX);
    }//GEN-LAST:event_jButton1ActionPerformed

