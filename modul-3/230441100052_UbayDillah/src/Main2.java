    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
        
    if (name.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Isi Nama Terlebih Dahulu!", "Pemberitahuan!", JOptionPane.INFORMATION_MESSAGE, iconX);
        return;
    }
    
    if (!january.isSelected() && !february.isSelected() && !march.isSelected() && !april.isSelected() && 
        !may.isSelected() && !june.isSelected() && !july.isSelected() && !august.isSelected() && 
        !september.isSelected() && !october.isSelected() && !november.isSelected() && !december.isSelected()) {
        JOptionPane.showMessageDialog(null, "Pilih minimal satu bulan!", "Pemberitahuan!", JOptionPane.INFORMATION_MESSAGE, iconX);
        return;
    }
    
    if (SPP.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Isi SPP Terlebih Dahulu!", "Pemberitahuan!", JOptionPane.INFORMATION_MESSAGE, iconX);
        return;
    }
    
    try {
        DefaultTableModel model = (DefaultTableModel) paymentTable.getModel();
        String nama = name.getText();
        String status = "Nunggak"; 
        int utang = 0; 
        int sppPerBulan = Integer.parseInt(SPP.getText());
        
        int jumlahBulan = 0;
        
        if (january.isSelected()) {
            jumlahBulan++;
        }
        
        if (february.isSelected()) {
            jumlahBulan++;
        }
        
        if (march.isSelected()) {
            jumlahBulan++;
        }
        
        if (april.isSelected()){
            jumlahBulan++;
        }
        
        if (may.isSelected()){
            jumlahBulan++;
        }
        
        if (june.isSelected()){
            jumlahBulan++;
        }
        if (july.isSelected()){
            jumlahBulan++;
        }
        if (august.isSelected()){
            jumlahBulan++;
        }
        
        if (september.isSelected()){
            jumlahBulan++;
        }
        if (october.isSelected()){
            jumlahBulan++;
        }
        if (november.isSelected()){
            jumlahBulan++;
        }
        if (december.isSelected()){
            jumlahBulan++;
        }
        
        if (jumlahBulan == 12) {
            status = "lunas";
        }

        utang = sppPerBulan * (12 - jumlahBulan);
        
        Object[] rowData = new Object[15];
        rowData[0] = nama;
        rowData[1] = status;
        rowData[2] = utang;
        rowData[3] = january.isSelected();
        rowData[4] = february.isSelected();
        rowData[5] = march.isSelected();
        rowData[6] = april.isSelected();
        rowData[7] = may.isSelected();
        rowData[8] = june.isSelected();
        rowData[9] = july.isSelected();
        rowData[10] = august.isSelected();
        rowData[11] = september.isSelected();
        rowData[12] = october.isSelected();
        rowData[13] = november.isSelected();
        rowData[14] = december.isSelected();
        
        model.addRow(rowData);
        
        resetForm();
        
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Format SPP tidak valid. Masukkan angka.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_sendActionPerformed

