    private void succes(String obat) {
        JOptionPane.showMessageDialog(null, "Pembelian " + obat + " berhasil, silahkan ambil di meja", "pemberitahuan", JOptionPane.INFORMATION_MESSAGE);

        anjay.setSelectedIndex(0);
        buttonGroup1.clearSelection();
}
    
    private void SIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SIMPANActionPerformed
        // TODO add your handling code here:
        int butuhobatIndex = anjay.getSelectedIndex();
        
        if (buttonGroup1.getSelection() == null){
            JOptionPane.showMessageDialog(null, "pilih obat terlebih dahulu", "Pemberitahuan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (butuhobatIndex == 0 && RBamoxilin.isSelected()) {
            succes("abotil");
        } else if (butuhobatIndex == 1 && RBmixgrib.isSelected()) {
            succes("betadine");
        } else if (butuhobatIndex == 2 && RBentrostop.isSelected()) {
            succes("diapet");
        }else if (butuhobatIndex == 3 && RBviks.isSelected()){
            succes("insto");
        }else if (butuhobatIndex == 4 &&  RBparacetamol.isSelected()){
            succes("mefinal");
        }else if (butuhobatIndex == 5 &&  RBoskadon.isSelected()){
            succes("mixagrip");
        }else if (butuhobatIndex == 6 && RBranitidin.isSelected()){
            succes("panadol");
        }else if (butuhobatIndex == 7 && RBibuproden.isSelected()){
            succes("obh");
        }else if (butuhobatIndex == 8 && RBhidrokortisonil.isSelected()){
            succes("promag");
        }else if (butuhobatIndex == 9 && RBlastagec.isSelected()){
            succes("vitaminc");
        }else {
            JOptionPane.showConfirmDialog(null, "Pembelian gagal dikarenakan obat tidak cocok dengan penyakit ( ANDA BISA MATI) ", "pemberitahuan!", JOptionPane.INFORMATION_MESSAGE);
        
        }
        
    }//GEN-LAST:event_SIMPANActionPerformed

