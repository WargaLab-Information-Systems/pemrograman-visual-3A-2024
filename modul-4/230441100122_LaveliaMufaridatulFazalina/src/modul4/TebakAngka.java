    private void btntebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntebakActionPerformed
      try {
        int tebakan = Integer.parseInt(tftebak.getText());
        boolean isCorrect = false;  
        sisaTebakan--; 
        
        if (tebakan == angkaAcak) {
            JOptionPane.showMessageDialog(null, "Anda Benar!");
            tfcek.setText("Aku adalah angka " + angkaAcak + "!"); 
            String nama = JOptionPane.showInputDialog("Masukkan nama Anda:");
            int skor = sisaTebakan * 10;
            modelSkor.addElement(nama + " - " + skor + " poin");
            listSkor.setModel(modelSkor); 
            //tfcek.setText("Aku adalah angka " + angkaAcak + "!"); 
            tfscore.setText(skor + " poin");
            isCorrect = true;
            
            resetPermainan();
         
            
        } else if (tebakan < angkaAcak) {
            tfcek.setText("Angka terlalu kecil");
            tftebak.setText(""); 
        } else {
            tfcek.setText("Angka terlalu besar");
            tftebak.setText(""); 
        }

        
        if (!isCorrect) {
            tfscore.setText("Sisa tebakan: " + sisaTebakan);
        }
        
        if (sisaTebakan == 0 && !isCorrect) {
            JOptionPane.showMessageDialog(null, "Anda Gagal! Angka yang benar adalah " + angkaAcak);
            resetPermainan();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Masukkan angka yang valid!");
    }
    }//GEN-LAST:event_btntebakActionPerformed

