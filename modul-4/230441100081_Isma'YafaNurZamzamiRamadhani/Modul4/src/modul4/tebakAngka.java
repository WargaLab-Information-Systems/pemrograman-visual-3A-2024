    private void btnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTebakActionPerformed
        // TODO add your handling code here:
        btnReset.setEnabled(true);
        try{
            int Angka1 = Integer.parseInt(jTebak.getText());
            if (Angka1 > Tebakangka){
                Kesempatan -= 1;
                btnTebak.setText("Tebak (" +Kesempatan+ ")");
                jHasiltebak.setText("Tebakan Anda Terlalu Besar");
                jScore.setText("0");
                jTebak.setText("");
                if(Kesempatan == 0){
                   JOptionPane.showMessageDialog(this, "Kesempatan Anda Habis \nAngka akan di reset", "Ada Inpo Nichhh", JOptionPane.INFORMATION_MESSAGE);
                   resetgame();
                }
            }else if (Angka1 < Tebakangka){
                Kesempatan -= 1;
                btnTebak.setText("Tebak (" +Kesempatan+ ")");
                jHasiltebak.setText("Tebakan Anda Terlalu Kecil");
                jScore.setText("0");
                jTebak.setText("");
                if(Kesempatan == 0){
                   JOptionPane.showMessageDialog(this, "Kesempatan Anda Habis \nAngka akan di reset", "Anda Inpo Nichhh", JOptionPane.INFORMATION_MESSAGE);
                   resetgame();
                }
            }else if (Angka1 == Tebakangka){
                jHasiltebak.setText(String.valueOf(Tebakangka));
                int Score = Kesempatan * 10;
                jScore.setText(String.valueOf(Score));
                JOptionPane.showMessageDialog(this, "Selamat Jawaban Anda Benarrr", "Congrasttt", JOptionPane.INFORMATION_MESSAGE);
                
                String Nama = JOptionPane.showInputDialog(this, "Silahkan Masukkan Nama Anda", "INPO INPOO", JOptionPane.INFORMATION_MESSAGE);
            
                if (Nama != null && !Nama.trim().isEmpty()) {
                    riwayatSkor.add("Nama : "+Nama + ", " +"Score : " + Score);
                }
                StringBuilder hasilscore = new StringBuilder();
                for (String skor : riwayatSkor){
                    hasilscore.append(skor).append("\n");
                }
                hasilScore.setText(hasilscore.toString());
                
                resetgame();
            }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "MASUKKAN ANGKA BANGG!!", "PERINGATANNN", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTebakActionPerformed

