    private void btntebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntebakActionPerformed

        try{
            int tebakan = Integer.parseInt(tftebak.getText());
            

            if (tebakan < bilRandom) {
                tfcek.setText("Angka terlalu kecil! " );
                kesempatan--;
                
            } else if (tebakan > bilRandom) {
               tfcek.setText("Angka terlalu besar!  " );
               kesempatan--;
               
            } else {
                JOptionPane.showMessageDialog(null, "Selamat, Anda Benar Yeayy🎉");
                String nama = JOptionPane.showInputDialog(this, "Masukkan Nama Anda:");
                int skor = kesempatan * 10;
                String skorItem = nama + " - Score: " + skor;
                tfcek.setText("Aku adalah " + tebakan);

                tfscore.setText(String.valueOf(skor));

                modelScore.addElement(skorItem); 
                JOptionPane.showMessageDialog(this, "Skor Anda: " + skor);
//                tfscore.setText(""+skor);
                
                resetGame();
                tftebak.setText("");
                tfcek.setText("");
                tfscore.setText("");
                return;
            }
                btntebak.setText("TEBAK (" + kesempatan + ")");
                
            if (kesempatan <= 0) {
                JOptionPane.showMessageDialog(this, "Yaa GAME OVER😓!!!.");
                tfcek.setText("Aku adalah " + bilRandom);
                resetGame();
            }
            } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!");
            }
    }//GEN-LAST:event_btntebakActionPerformed

