    private int nomor = 1;

    private void btnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTebakActionPerformed
        // TODO add your handling code here:          
        String tebakan = String.valueOf(angkaTebakan);//mengubah nilai pd angkaTebakan menjadi string lalu disimpan di tebakan
        String input = TFtebak.getText();// getText dari TFtebak lalu disimpan di input
        int angkaAsal = Integer.parseInt(input); //konversi var input menjadi int lalu di simpan di angkaAsal

        if (kesempatan > 0) {
            if (input.equals(tebakan)) {//jika tebakan benar
                TFcek.setText("yeyy benar aku adalah angka " + tebakan);
                skor = kesempatan * 10; 
                TFscore.setText(String.valueOf(skor));//nampilin score di TFscore
                JOptionPane.showMessageDialog(null, "ASIKKKK BENERRR NIH");
                String nama;
                nama = JOptionPane.showInputDialog(null, "Masukkan Nama Anda:");
                
                TA.append(nomor++ +". "+ "Nama : " + nama + ", Skor : " + skor + "\n");
                
                //ngatur ulang tebakannya
                kesempatan = 10; 
                angkaTebakan = asal.nextInt(101); 
                
            } else if (angkaAsal > 100){
                JOptionPane.showMessageDialog(null, "Tebakan rentang nya 0 - 100");
                    return;
            }else {
                kesempatan--;
                btnTebak.setText("Tebak " + "("+kesempatan+")");
                TFscore.setText(String.valueOf(kesempatan * 10));

                if (angkaAsal > angkaTebakan) {
                    TFcek.setText("waduhhh terlalu BESAR angkanya");
                } else if (angkaAsal < angkaTebakan) {
                    TFcek.setText("yahhhhh terlalu KECIL angkanya");
                }
                

                if (kesempatan == 0) {
                    JOptionPane.showMessageDialog(null, "yahhh gagal, coba lagi yaa");
                    resetTebakan();
                }
            }
        }
    }//GEN-LAST:event_btnTebakActionPerformed
        
    private void resetTebakan() {
        kesempatan = 10;
        angkaTebakan = asal.nextInt(101);
        TFtebak.setText("");     
        TFcek.setText("");
        TFscore.setText("");
        btnTebak.setText("Tebak (10)");
    }
    
