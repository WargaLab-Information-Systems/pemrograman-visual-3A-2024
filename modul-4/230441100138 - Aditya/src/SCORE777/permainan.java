    private void cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekActionPerformed
        try {
            int tebakan = Integer.parseInt(tebak.getText());
            percobaan++;
            System.out.println("" + angkaTebakan);
            if (tebakan <= 0 || tebakan > 100) {
                JOptionPane.showMessageDialog(this,
                    "Masukkan nilai dari angka 1 - 100",
                    "PERINGATAN",
                    JOptionPane.WARNING_MESSAGE);
            } else if (tebakan < angkaTebakan) {
                info.setText("Nilai rendah!");
                JOptionPane.showMessageDialog(this,
                    "Angka tebakan terlalu rendah",
                    "HADEEEEHH",
                    JOptionPane.WARNING_MESSAGE);
            } else if (tebakan > angkaTebakan) {
                info.setText("Angka Tebakan Ketinggian");
                JOptionPane.showMessageDialog(this,
                    "Angka tebakan terlalu tinggi",
                    "HADEEEEHH",
                    JOptionPane.WARNING_MESSAGE);
            } else {
                int totalScore = (maksPercobaan - percobaan) * 10;
                skor.setText("" + totalScore);
                info.setText("Anda Berhasil! Angka Yang Benar " + tebakan);
                JOptionPane.showMessageDialog(this,
                    "\nSelamat Kamu Menjawab Dengan Benar !!!\nKamu Termasuk Golongan Jenius \nAngka Tebakan : " + angkaTebakan,
                    "SELAMAT JAWABAN BENAR",
                    JOptionPane.INFORMATION_MESSAGE);

                String name;
                boolean nameExists;
                do {
                    name = JOptionPane.showInputDialog(this, "Masukkan nama Kamu : ");
                    if (name == null) {
                        JOptionPane.showMessageDialog(this,
                            "Membatalkan Inputan",
                            "PERINGATAN",
                            JOptionPane.INFORMATION_MESSAGE);
                        reset();
                        return;
                    } else if (name.trim().isEmpty()) {
                        JOptionPane.showMessageDialog(this,
                            "Nama Anda Kosong",
                            "PERINGATAN",
                            JOptionPane.INFORMATION_MESSAGE);
                        reset();
                        return;
                    }
                    // Cek apakah nama sudah ada di list
                    nameExists = false;
                    for (int i = 0; i < listScoreModel.getSize(); i++) {
                        if (listScoreModel.getElementAt(i).toString().contains(name)) {
                            nameExists = true;
                            break;
                        }
                    }
                    if (nameExists) {
                        JOptionPane.showMessageDialog(this,
                            "Nama sudah terdaftar. Silakan masukkan nama lain.",
                            "PERINGATAN",
                            JOptionPane.WARNING_MESSAGE);
                    }
                } while (nameExists);

                JOptionPane.showMessageDialog(this,
                    "Selamat : " + name.toUpperCase() + " Skor Kamu Sudah Di Data \nSilahkan Dilihat",
                    "TAHNIAA",
                    JOptionPane.INFORMATION_MESSAGE);
                listScoreModel.addElement((listnomor++) + ". " + name + " - Skor: " + totalScore);
                reset();
            }

            if (percobaan >= maksPercobaan) {
                JOptionPane.showMessageDialog(this,
                    "Kamu Gagal! Angka tebakan yang benar adalah : " + angkaTebakan,
                    "HADEEEEHH",
                    JOptionPane.INFORMATION_MESSAGE);
                reset();
            } else {
                cek.setText("Nyawa ( " + (maksPercobaan - percobaan) + " )");
            }
        } catch (NumberFormatException ex) {
            percobaan++;
            System.out.println("ANJAY");
            JOptionPane.showMessageDialog(this,
                "Masukkan setidaknya 1 angka",
                "PERINGATAN",
                JOptionPane.WARNING_MESSAGE);
            cek.setText("Nyawa ( " + (maksPercobaan - percobaan) + " )");
        }



    }//GEN-LAST:event_cekActionPerformed

