    private void btnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTebakActionPerformed
        // TODO add your handling code here:
        int b;
        if (a > 0){
            try{
                int x = Integer.parseInt(tebak.getText());
                if (x < bilRndm){
                    cek.setText("angka terlalu kecil");
                    btnTebak.setText("TEBAK" + "(" + a + ")");
                    b = 10 * a;
                    a--;
                    score.setText(Integer.toString(b));
                    System.out.println(bilRndm);
                } else if (x > bilRndm){
                    cek.setText("angka terlalu besar");
                    btnTebak.setText("TEBAK" + "(" + a + ")");
                    b = 10 * a;
                    a--;
                    score.setText(Integer.toString(b));
                } else {
                    String input = JOptionPane.showInputDialog("Masukkan Nama :");
                    list.add(String.valueOf(input + " - Score " + score.getText()));
                    String tampilan = String.join(",",list);
                    hasil.setText(tampilan);
//                    hasil.setText(input + " - Score " + score.getText());
                    reset();
                }
            } catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Masukkan angka untuk menebak", "Peringatan", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Anda Gagal Menebak", "Try Again", JOptionPane.ERROR_MESSAGE);
            reset();
        }
    }//GEN-LAST:event_btnTebakActionPerformed

