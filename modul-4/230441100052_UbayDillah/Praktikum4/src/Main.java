    private int generateNewRandomNumber() {
        Random random = new Random();
        return random.nextInt(100) + 1;
    }
    
    private void reset() {
        guess.setText("");
        check.setText("");
        score.setText("");
    }
    
    int guessNumber = generateNewRandomNumber();
    int init = 0;
    
    private void guessButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guessButtonActionPerformed
        // TODO add your handling code here:
        init++;
        int limit = (10 - init) * 10;
        score.setText(Integer.toString(limit));

        String name = "";
        
        if (guess.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silahkan isi Coba Tebak Field", "Warning!", JOptionPane.INFORMATION_MESSAGE, xIcon);
            return;
        }
        
        if (init > 10) {
            init = 0;
            guessButton.setText("Tebak");
            JOptionPane.showMessageDialog(null, "Sayang sekali, Anda Kalah!", "Result!", JOptionPane.INFORMATION_MESSAGE, xIcon);
            reset();
            return;
        }
        
        int guessField = Integer.parseInt(guess.getText());
        

        if (guessField > guessNumber) {
            check.setText("Lebih Kecil");
        } else if (guessField < guessNumber) {
            check.setText("Lebih Besar");            
        } else {
            guessButton.setText("Tebak");

            check.setText("Benar! Angka adalah " + guessNumber);
            JOptionPane.showMessageDialog(null, "Selamat! Anda berhasil menebak angka dengan benar", "Result!", JOptionPane.INFORMATION_MESSAGE, centangIcon);

            // Meminta nama pengguna dan menambahkan ke history
            name = JOptionPane.showInputDialog("Masukkan nama Anda:");
            if (name == null || name.isEmpty()) {
                name = "Anonymous";
            }
            
            scoreList.add(name + " - Score: " + limit);
            listHistory.setListData(scoreList.toArray(new String[0]));
            reset();
            guessNumber = generateNewRandomNumber();
            init = 0;
            return;
        }
        
        guessButton.setText("Tebak (" + init + ")");
        
    }//GEN-LAST:event_guessButtonActionPerformed

    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        // TODO add your handling code here:
        reset();
        init = 0;
        guessButton.setText("Tebak");
        guessNumber = generateNewRandomNumber();
    }//GEN-LAST:event_resetButtonActionPerformed

