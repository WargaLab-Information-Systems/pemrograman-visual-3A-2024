    private void BtnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTebakActionPerformed
        // TODO add your handling code here:
        try{
            int tebakan = Integer.parseInt(Tebakan.getText());
            percobaan ++;
            System.out.println(""+tebakanAngka);
            
            if (tebakan <=0 || tebakan > 100){
                JOptionPane.showMessageDialog(this, null ,"Tidak Valid Cihuyyy", JOptionPane.WARNING_MESSAGE,iconInvalid);
            }else if(tebakan < tebakanAngka){
                CekTebakan.setText("Masih terlalu rendah");
                JOptionPane.showMessageDialog(this, "Terlalu Rendah kayak rank ML mu" ,"Waduh Kerendahan Paduka", JOptionPane.WARNING_MESSAGE,iconGagal);
                
            }else if(tebakan > tebakanAngka){
                CekTebakan.setText("Tebakan Ketinggian");
                JOptionPane.showMessageDialog(this, "Ketinggian Bang seperti Harapanmu" ,"Buset Ketinggian Paduka", JOptionPane.WARNING_MESSAGE,iconGagal);
            }
            else {
                int totalScore = (maksPercobaan-percobaan)*10;
                Score.setText(""+totalScore);
                CekTebakan.setText("Anda Berhasil!");
                JOptionPane.showMessageDialog(this, "\nSelamat Anda Benar !!!\nIQ anda diatas rata-rata \nAngka Tebakan : " +tebakanAngka,"cihuyy kamu termasuk orang IQ tinggi", JOptionPane.INFORMATION_MESSAGE,iconBerhasil);
                String name = JOptionPane.showInputDialog(this, "Masukkan nama Anda : ");
               
                if(name == null){
                    JOptionPane.showMessageDialog(this,"Input dibatalkan", "Input Canceled",JOptionPane.INFORMATION_MESSAGE,Bingung);
                    resetPermainan();
                }else if(name.trim().isEmpty()){
                    JOptionPane.showMessageDialog(this,"Nama Anda Kosong", "Input Tidak Valid",JOptionPane.INFORMATION_MESSAGE,Bingung);
                    resetPermainan();
                }else{
                    JOptionPane.showMessageDialog(this, "BAIKLAH PADUKA : " +name.toUpperCase()+" HASIL TEST PADUKA \nSUDAH KAMI CATAT","*EMOT (SIP SAYA CATAT) !!!", JOptionPane.INFORMATION_MESSAGE,iconBerhasil);
                     listScorePermainan.addElement((nomorList++)+". "+name+ " : Skor: " + totalScore);
                     resetPermainan();
                }
                return;
            }
             if (percobaan >= maksPercobaan) {
                JOptionPane.showMessageDialog(this, "Anda Gagal!" ,"Coba Lagi", JOptionPane.WARNING_MESSAGE,Maning);
                resetPermainan();
            } else {
                BtnTebak.setText("Tebak ( "+((maksPercobaan)-percobaan)+" )");
            }

            
        }catch(NumberFormatException ex){
            percobaan ++;
            System.out.println("cihuyyy y nya 3");
            JOptionPane.showMessageDialog(this, "Masukkan Angka Jangan Huruf Cuyyy" ,"Tidak Valid coiiiii", JOptionPane.WARNING_MESSAGE,iconInvalid);
            BtnTebak.setText("Tebak ( "+((maksPercobaan)-percobaan)+" )");
            
        }
        
    }//GEN-LAST:event_BtnTebakActionPerformed

