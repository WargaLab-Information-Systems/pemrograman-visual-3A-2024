    private void tebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tebakActionPerformed
        System.out.println(AngkaRandom);
        if (tangka.getText().isEmpty() && cek.getText().isEmpty() && score.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silhkan Klik start terlebih dahulu");
            return;
        }
        if (tangka.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Tidak boleh kosong harus di isi terlebih dahulu");
            return;
        }
        try{
            int tebakan = Integer.parseInt(tangka.getText());
            
//            System.out.println("Tebakan" + tebakan + "angka random :");
            
            if(tebakan < 1 || tebakan > 100){
                JOptionPane.showMessageDialog(null, "masukkan angka random dari 1-100");
                return;
            }
            
            
            if(AngkaRandom == tebakan){
                JOptionPane.showMessageDialog(null, "tebakan anda benar");
                cek.setText("Saya adalah angka : " + AngkaRandom);
                tebak.setEnabled(false);
                tangka.setEnabled(false);
                cek.setEnabled(false);
                score.setEnabled(false);
                int HasilScore = Kesempatan;
                int oke = HasilScore * 10;
                score.setText("Your Score :" + oke);
                String nama = JOptionPane.showInputDialog("masukkan nama anda :");
                if (nama != null && !nama.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Nama Anda Berhasil Ditambahkan");
                    list.add(nama + " Score: " + oke + "\n");
                        hasil.setText("");
                        for (int i = 0;i < list.size();i++) {
                        hasil.append((i+1) + "."+ list.get(i) + "\n");}
                }else{
                    JOptionPane.showMessageDialog(null, "Nama Anda Tidak Berhasil Di tambahkan");
                }
                return;
            }else if(tebakan < AngkaRandom){
                JOptionPane.showMessageDialog(null, "Tebakan anda lebih kecil");
                cek.setText("Saya adalah angka : " + AngkaRandom);
                Kesempatan--;
            }else if(tebakan > AngkaRandom){
                JOptionPane.showMessageDialog(null, "Tebakan anda lebih besar");
                cek.setText("Saya adalah angka :" + AngkaRandom);
                Kesempatan--;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "isi dengan angka bukan huruf");
            return;
        }
            if(Kesempatan == 0 ){
                tebak.setEnabled(false);
                JOptionPane.showMessageDialog(null,"Anda telah gagal dalam menebak angka cobalah beberapa saat lagi","Pemberitahuan",JOptionPane.ERROR_MESSAGE);
//                return;
            }
            tebak.setText("Tebak" + Kesempatan);
        
    }//GEN-LAST:event_tebakActionPerformed
    
