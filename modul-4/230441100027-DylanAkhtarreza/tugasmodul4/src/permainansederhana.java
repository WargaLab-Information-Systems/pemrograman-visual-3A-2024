    private void btnTebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTebakActionPerformed
        // TODO add your handling code here:
    String inpAngka = inputAngka.getText();

        if (inpAngka.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Input angka tidak boleh kosong", "Input angka", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
// untuk menangani error atau pengecualian yang terjadi pada operasi numerik
        try {
            int angka = Integer.parseInt(inpAngka);

            clickCount++;

            int sisakesempatan = countLimit - clickCount;
            btnTebak.setText("TEBAK " + "(" + Integer.toString(sisakesempatan) + ")");

            if (clickCount > countLimit) {
//                resetProgram();
                JOptionPane.showMessageDialog(this, "Anda Gagal", "Gagal", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (angka == angkaRandom) {
                int sisaKlik = countLimit - clickCount;
                int totalScore = sisaKlik * 10 + 10;
                hasil.setText("Aku adalah angka " + Integer.toString(angkaRandom));
                JOptionPane.showMessageDialog(this, "Anda Berhasil", "Berhasil", JOptionPane.INFORMATION_MESSAGE);

                String response = null;

                while (response == null || response.isEmpty()) {
                    response = JOptionPane.showInputDialog(this, "Masukkan nama anda:");

                    if (response == null || response.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Input tidak boleh kosong, harap masukkan nama anda.", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        score.setText(Integer.toString(totalScore));
                        String skorPermainan = response + " : " + totalScore;
                        riwayatScore.add(skorPermainan); // Tambahkan skor ke riwayat
                        updateScoreArea(); // Perbarui TextArea
                        JOptionPane.showMessageDialog(this, "Score anda disimpan");
                    }
                }

//                resetProgram();
            } else if (angka < angkaRandom) {
                hasil.setText("Aku lebih besar");
                score.setText("0");
            } else if (angka > angkaRandom) {
                hasil.setText("Aku lebih kecil");
                score.setText("0");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Yang anda inputkan bukan angka", "Input Tebak", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnTebakActionPerformed

